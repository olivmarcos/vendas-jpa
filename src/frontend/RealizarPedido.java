    private void btnRemoverPedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverPedidoMouseClicked
        int[] linhas = pedidoTabela.getSelectedRows();
        javax.swing.table.DefaultTableModel padrao = (javax.swing.table.DefaultTableModel) pedidoTabela.getModel();
        for (int i = 0; i < linhas.length; i++) {
            String codigoPedido = String.valueOf(padrao.getValueAt(linhas[i], 0));
            padrao.removeRow(linhas[i]);

            int cod = Integer.parseInt(codigoPedido);

            try {
                PedidoCtrl pedidoCtrl = new PedidoCtrl();
                Pedido pedido = pedidoCtrl.recover(cod);
                PedidoFacade pedidoFacade = new PedidoFacade();
                pedidoFacade.desfazerPedido(pedido);
                JOptionPane.showMessageDialog(this, "Pedido removido com sucesso!");
            } catch (SQLException ex) {
                Logger.getLogger(RealizarPedido.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnRemoverPedidoMouseClicked

