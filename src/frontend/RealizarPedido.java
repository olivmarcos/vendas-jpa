    private void btnSalvarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarPedidoActionPerformed

        String produtoItem = (String) produtoCombobox.getSelectedItem();
        String vendedorItem = (String) vendedorCombobox.getSelectedItem();
        String clienteItem = (String) clienteCombobox.getSelectedItem();

        Double valorPedidoProduto = 0.00;
        int quantidade = 0;
        if (valorInput.getText().equals("") || quantidadeInput.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Por favor informe os valores corretos!");
            return;
        } else {
            valorPedidoProduto = Double.parseDouble(valorInput.getText());
            quantidade = Integer.parseInt(quantidadeInput.getText());
        }

        int codigoProduto = Character.getNumericValue(produtoItem.charAt(0));
        int codigoVendedor = Character.getNumericValue(vendedorItem.charAt(0));
        int codigoCliente = Character.getNumericValue(clienteItem.charAt(0));

        try {
            ProdutoCtrl produtoController = new ProdutoCtrl();
            Produto produto = produtoController.recover(codigoProduto);

            VendedorCtrl vendedorController = new VendedorCtrl();
            Vendedor vendedor = vendedorController.recover(codigoVendedor);

            Controller clienteController = new Controller();
            Cliente cliente = clienteController.recover(codigoCliente);

            Pedido pedido = new Pedido();
            pedido.setPed_data(new Date());
            pedido.setPed_observacao("Compra do produto  " + produto.getProd_descricao() + " do vendedor " + vendedor.getVend_nome() + " na data " + pedido.getPed_data());
            pedido.setPed_cod_cliente(cliente.getCli_codigo());
            pedido.setPed_cod_vendedor(vendedor.getVend_codigo());

            PedidoCtrl pedidoController = new PedidoCtrl();
            if (produto.getProd_saldo() <= 0) {
                JOptionPane.showMessageDialog(this, "Não há produto no Estoque!");
                return;
            }
            pedidoController.criarPedido(cliente, vendedor, pedido, produto, valorPedidoProduto, quantidade);
            this.preencherTabela();
            valorInput.setText("");
            quantidadeInput.setText("");
        } catch (Exception err) {
            System.err.println("Erro ao salvar o pedido" + err.getMessage());
        }
    }//GEN-LAST:event_btnSalvarPedidoActionPerformed

