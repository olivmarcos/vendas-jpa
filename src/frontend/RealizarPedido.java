    public void preencherComboboxProduto() throws SQLException {
        ProdutoDao dao = new ProdutoDao();
        ArrayList<Produto> produtos = dao.recoverAll();
        produtoCombobox.removeAllItems();
        for (int i = 0; i < produtos.size(); i++) {
            produtoCombobox.addItem(produtos.get(i).toString());
        }
    }

    public void preencherComboboxVendedor() throws SQLException {
        VendedorDao dao = new VendedorDao();

        ArrayList<Vendedor> vendedores = dao.recoverAll();
        vendedorCombobox.removeAllItems();
        for (int i = 0; i < vendedores.size(); i++) {
            vendedorCombobox.addItem(vendedores.get(i).toString());
        }
    }

    public void preencherComboboxCliente() throws SQLException {
        ClienteDao dao = new ClienteDao();

        ArrayList<Cliente> clientes = dao.recoverAll();
        clienteCombobox.removeAllItems();
        for (int i = 0; i < clientes.size(); i++) {
            clienteCombobox.addItem(clientes.get(i).toString());
        }
    }

    public void preencherTabela() throws SQLException {
        DefaultTableModel padrao = (DefaultTableModel) pedidoTabela.getModel();
        padrao.setNumRows(0);

        PedidoCtrl pedidoController = new PedidoCtrl();
        String[][] dados = pedidoController.recoverAll();

        for (int i = 0; i < dados.length; i++) {
            padrao.addRow(dados[i]);
        }
    }

